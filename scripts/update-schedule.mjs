/**
 * Refresh src/data/matches.ts from the open-source 26worldcup dataset, which
 * mirrors FIFA's official schedule and results.
 *
 *   node scripts/update-schedule.mjs
 *
 * Re-run during the tournament to pull the latest scores and resolved knockout
 * fixtures. Requires network access.
 */
import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const BASE = "https://raw.githubusercontent.com/26worldcup/26worldcup.github.io/main/public/data";
const here = dirname(fileURLToPath(import.meta.url));
const OUT = resolve(here, "../src/data/matches.ts");

async function getJson(name) {
  const res = await fetch(`${BASE}/${name}`);
  if (!res.ok) throw new Error(`Failed to fetch ${name}: HTTP ${res.status}`);
  return res.json();
}

const [{ matches: raw }, { venues }, meta] = await Promise.all([
  getJson("matches.json"),
  getJson("venues.json"),
  getJson("meta.json"),
]);

const venueName = (id) => {
  const v = venues[id];
  return v ? `${v.realName} · ${v.city}` : "TBD";
};

const matches = raw
  .sort((a, b) => a.n - b.n)
  .map((x) => ({
    n: x.n,
    stage: x.stage,
    group: x.group,
    date: x.date,
    venue: venueName(x.venueId),
    status: x.status,
    home: { code: x.home?.code ?? null, score: x.home?.score ?? null, pen: x.home?.pen ?? null },
    away: { code: x.away?.code ?? null, score: x.away?.score ?? null, pen: x.away?.pen ?? null },
    phA: x.phA ?? null,
    phB: x.phB ?? null,
    winner: x.winner ?? null,
  }));

const out = `import type { GroupId } from "../types";

// 2026 FIFA World Cup full match schedule — all 104 matches (72 group + 32
// knockout). Group results are filled in as matches are played; knockout slots
// (e.g. "1A", "2B", "W73", "3ABCDF") resolve to teams once earlier rounds
// finish. See labelSlot() in components/Schedule.tsx for human-readable slots.
//
// Generated by scripts/update-schedule.mjs from the open-source 26worldcup
// dataset (github.com/26worldcup/26worldcup.github.io, public/data), which
// mirrors FIFA's official schedule and results. Do not edit by hand.
// Snapshot updatedAt: ${meta.updatedAt}

export type Stage = "group" | "r32" | "r16" | "qf" | "sf" | "third" | "final";

export interface MatchSide {
  /** Team id (matches Team.id) once known, else null for an unresolved slot. */
  code: string | null;
  score: number | null;
  /** Penalty-shootout score, or null if not applicable. */
  pen: number | null;
}

export interface Match {
  /** Official match number, 1-104. */
  n: number;
  stage: Stage;
  group: GroupId | null;
  /** Kick-off, ISO 8601 UTC. */
  date: string;
  venue: string;
  status: "finished" | "scheduled" | string;
  home: MatchSide;
  away: MatchSide;
  /** Bracket slot token for the home side, e.g. "A1", "1A", "W73". */
  phA: string | null;
  phB: string | null;
  /** Winning team id once decided, else null. */
  winner: string | null;
}

export const SCHEDULE_UPDATED_AT = ${JSON.stringify(meta.updatedAt)};

export const MATCHES: Match[] = ${JSON.stringify(matches, null, 2)};
`;

writeFileSync(OUT, out);
const played = matches.filter((m) => m.status === "finished").length;
console.log(`Wrote ${OUT}\n${matches.length} matches, ${played} played, snapshot ${meta.updatedAt}`);
